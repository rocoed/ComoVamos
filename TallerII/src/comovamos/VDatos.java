        public void ctabla(){
         modelo = new DefaultTableModel();
        tabla.setModel(modelo);
        try {
            //tabla.setEnabled(false);
            con=new Conexion("tallerii1202","paul123");
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo acceder al servidor de la Base de Datos, verifique que tiene conexion con acceso al servidor", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
        res=con.select(con, campos, tablas, condicion);
        con.desconectar();
        int n=1;
                
      
        try {//obtenemos los nombres de las columnas y armamos la tabla
         tdatos=new String[res.getMetaData().getColumnCount()];
         while(n<(res.getMetaData().getColumnCount()+1)){   
            modelo.addColumn(res.getMetaData().getColumnLabel(n));
            
            n++;}
            int p=0;
         for(int f=1;f<n;f++) {
            tdatos[p]=res.getMetaData().getColumnTypeName(f);
            p++;
        }	
                  	
                 
        } catch (SQLException ex) {
            Logger.getLogger(VDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
      /////////////////////obtener datos de la consulta y meterlos a la tabla
         try {
             
             datos=new String[res.getMetaData().getColumnCount()];
                                
                while(res.next()){
                    for(int i=0;i<res.getMetaData().getColumnCount();i++){
                        System.out.println(""+tdatos[i]);
                        if(tdatos[i]=="INT"){datos[i]=String.valueOf(res.getInt(i+1));}
                        if(tdatos[i]=="VARCHAR"){datos[i]=res.getString(i+1);}
                        if(tdatos[i]=="DATETIME"){datos[i]=""+res.getDate(i+1).getDay()+"/"+res.getDate(i+1).getMonth()+"/"+(res.getDate(i+1).getYear()+1900);}
                        
                    }
                    modelo.addRow(datos);
                }
                                 
        } catch (SQLException ex) {
            Logger.getLogger(VDatos.class.getName()).log(Level.SEVERE, null, ex);
        }
         for(int f=0;f<7;f++) {
            System.out.println("Dato: "+(f+1)+": "+datos[f]);
            
        }	
        
    }
